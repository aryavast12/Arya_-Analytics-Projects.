def celsius_to_fahrenheit(c):
    """Converts Celsius to Fahrenheit."""
    return (c * 9/5) + 32

def fahrenheit_to_celsius(f):
    """Converts Fahrenheit to Celsius."""
    return (f - 32) * 5/9

def celsius_to_kelvin(c):
    """Converts Celsius to Kelvin."""
    return c + 273.15

import temperature

def main():
    """Asks the user for a temperature and conversion type, then displays the result."""
    print("--- Temperature Converter ---")
    print("1. Celsius to Fahrenheit")
    print("2. Fahrenheit to Celsius")
    print("3. Celsius to Kelvin")

    choice = input("Choose a conversion (1-3): ")
    temp_str = input("Enter the temperature to convert: ")

    try:
        temp = float(temp_str)
        if choice == '1':
            result = temperature.celsius_to_fahrenheit(temp)
            print(f"{temp}°C is equal to {result:.2f}°F")
        elif choice == '2':
            result = temperature.fahrenheit_to_celsius(temp)
            print(f"{temp}°F is equal to {result:.2f}°C")
        elif choice == '3':
            result = temperature.celsius_to_kelvin(temp)
            print(f"{temp}°C is equal to {result:.2f}K")
        else:
            print("Invalid conversion choice. Please run the program again.")

    except ValueError:
        print("Invalid temperature. Please enter a number.")
if __name__ == "__main__":
    main()